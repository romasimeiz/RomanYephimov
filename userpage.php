    <img class="img-thumbnail" src="/img/korotych.jpg">
    <p>Посёлок городского типа Коротыч находится на расстоянии менее чем в 1 км от реки Уды (правый берег). На расстоянии в 1 км расположены посёлки Коммунар и Рай-Оленовка. Через посёлок проходит железная дорога Харьков - Полтава, станция Коротич. Рядом проходит автомобильная дорога Р-51 (М-03-E 40</p>
    <p>Название Коротич, предположительно происходит от пересыхающей летом реки, протекавшей через посёлок во время дождей и таяния снега — короткое течение (Коротич).</p>
    <p>Основан в середине XVII века, уже в 1697 году действовала Покровская церковь с колокольней. В 1845 году на месте деревянного храма построен новый каменный со звонницей. (до настоящего времени не сохранился — сейчас построен новый Свято-Покровский храм). По переписи населения 1864 года в селе Коротич насчитывалось 125 дворов, 703 жителя. В начале XX века количество жителей было более двух тысяч человек. Через посёлок проходит железнодорожная линия Харьков — Люботин, которая построена в 70-х годах XIX века, называемая тогда Харьково-Николаевская железная дорога направления Харьков — Кременчуг.</p>
