        <h2>Изюм - Каменка - Глубокая Макатиха</h2>
        <h5><span class="glyphicon glyphicon-time">02.03.2016, 08:36</span></h5>
        <h5><span class="label label-danger">Уже были</span></h5>
        <img class="img-thumbnail center" src="/img/blog/rogoz.jpeg">
         <p>Рогозянское водохранилище (укр. Рогозянське водосховище) — небольшое русловое водохранилище на реке Уды, расположенное в Золочевском районе Харьковской области.</p>

         <p><strong>Первый день.</strong> <br> От Изюма до Каменки можно доехать на автобусе. В получасе ходьбы от трассы Харьков — Ростов, пересекающей село, вверх по течению реки Каменки расположена  плотина. По ней нужно перейти на левый берег.</p>

        <p>По предположению историков вдоль Каменки продвигалась дружина князя Игоря. Кстати, эта местность связана еще с одним историческим событием. В центре села Каменка у здания Дома культуры стоит памятник декабристам — рассказывает президент Харьковского клуба любителей путешествий Юрий Телешевский.</p>
        <img class="img-thumbnail float-left" src="/img/blog/rogoz1.jpg">
        <p>Справа у плотины нужно подняться на береговую террасу. На противоположном краю долины заметны ярко-желтые пятна. Это каменоломни. На пути вдоль левого берега встречается немало красивых обломков кремня. В верхней части долины Каменки находится село Долгенькое. Здесь можно остановиться на ночь, а утром отправиться к реке Макатихе.</p>

        <p>Историки считают, что Макатиха - это и есть река Каяла, на берегах которой произошла битва русских войск с половцами, - говорит Юрий Телешевский.</p>

        <p><strong>Второй день. </strong> <br> От села Долгенькое двигаемся к верховьям реки Долины. В этом направлении нет благоустроенных дорог, но встречаются наезженные полеводами колеи. По ним и следует идти в верховья балки, слегка забирая влево. Поселок Долина растянулся на несколько километров. С высокого кургана, расположенного к северу от населенного пункта, хорошо просматривается река и ее долина.</p>
        <img class="img-thumbnail float-left" src="/img/blog/rogoz2.jpg">
        <p>Примерно через два часа ходьбы на пути встретится село Крестище. Здесь можно купить продукты и пополнить запасы воды. Из Крестищ к селу Глубокая Макатиха ведет асфальтированная дорога.</p>

        <p>Село Глубокая Макатиха расположено на склонах крутой балки. По ней протекает незаметная речушка Макатиха. Поход вдоль реки можно начать там, где дорога пересекает балку, у колодца.</p>

        <p>Воды сейчас в реке очень мало. Восемьсот лет назад Макахита была гораздо полноводнее. В 3–4 км от села балка покрыта зарослями деревьев и кустарников. Здесь можно заночевать с палатками, - советует Юрий Телешевский.</p>

        <p><strong>Третий день.</strong> <br> В Изюм можно вернуться из села Крестище — неподалеку от него есть автобусная остановка. Второй вариант — пройти к трассе Ростов— Харьков, расположенной в 4–5 км вверх по Макатихе, у самых ее истоков. Выйдя на шоссе, вы доберетесь до Изюма на попутном автобусе.</p>
